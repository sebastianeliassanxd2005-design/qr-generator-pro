// Función para cambiar idioma
function setLanguage(lang) {
    const translations_data = translations[lang];
    
    // Guardar preferencia
    localStorage.setItem('preferredLanguage', lang);
    
    // Actualizar todos los elementos con data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations_data[key]) {
            if (element.innerHTML.includes('<')) {
                element.innerHTML = translations_data[key];
            } else {
                element.textContent = translations_data[key];
            }
        }
    });
    
    // Actualizar botón de idioma
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.textContent = translations_data['lang-toggle'];
    }
    
    // Actualizar atributos placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations_data[key]) {
            element.placeholder = translations_data[key];
        }
    });
    
    // Actualizar atributos title
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        if (translations_data[key]) {
            element.title = translations_data[key];
        }
    });
}

// Inicializar idioma
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'es';
    setLanguage(savedLang);
    
    // Toggle de idioma
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', (e) => {
            e.preventDefault();
            const currentLang = localStorage.getItem('preferredLanguage') || 'es';
            const newLang = currentLang === 'es' ? 'en' : 'es';
            setLanguage(newLang);
        });
    }
});
